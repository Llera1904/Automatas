from turtle import *

def dibujoProtocolo():
    speed(speed=0)
    #Ventana
    setup(640, 680, 0, 0)
    title("Ventana del dibujo")
    screensize(10, 10)
    hideturtle()
    bgcolor("gray")
    #Punto de referencia del eje (0,0)
    #dot(10, 0, 0, 0)
    penup()
    #Ubicacion del primer circulo
    fillcolor("white")
    goto(-150, 150)
    pendown()
    begin_fill()
    circle(50)
    end_fill()
    penup()
    #Ubicacion de la primera linea
    goto(-100, 200)
    pendown()
    goto(100, 200)
    penup()
    #Dibujar el semicirculo
    goto(150, 210)
    pendown()
    circle(30)
    penup()
    #Ubicacion del segundo circulo
    goto(150, 150)
    pendown()
    begin_fill()
    circle(50)
    end_fill()
    penup()
    #Ubicaion de la segunda linea
    # goto(150, -50)
    # pendown()
    # goto(0, -100)
    # goto(-150, -50)
    goto(0, 100)
    pendown()
    circle(150, -54)
    dot(10, 0, 0, 0)
    penup()
    #goto(60, 60) #posicion random
    circle(150, 54) #circulo random para volver a 360º
    # penup()
    # goto(0, -100)
    pendown()
    circle(150, 54)
    penup()
    circle(150, -54) #circulo random para volver a 360°
    #Dibujo del automata de paridad
    goto(-150, 90)
    pendown()
    goto(150, 90)
    goto(150, -210)
    goto(-150, -210)
    goto(-150, 90)
    #Dibujando las lineas que conectan los circulos
    penup()
    goto(0, -155)
    pendown()
    circle(110)
    penup()
    #Dibuja lineas que conectan los circulos
    goto(0, -10)
    pendown()
    circle(75, 43)
    penup()
    circle(75, -43)
    goto(0, -10)
    pendown()
    circle(75, -43)
    dot(10, 0, 0, 0)
    penup()
    circle(75, 43)
    goto(-100, -135)
    pendown()
    circle(75, 180)
    penup()
    goto(0, -110)
    pendown()
    circle(75, -43)
    dot(10, 0, 0, 0)
    penup()
    circle(75, 43)
    goto(0, -110)
    pendown()
    circle(75, 43)
    penup()
    circle(75, -43)
    circle(75, 180)
    goto(100, -135)
    pendown()
    circle(75, -180)
    penup()
    circle(75, 180)
    #Dibujando circulos
    penup()
    goto(-75, -10)
    pendown()
    begin_fill()
    circle(25)
    end_fill()
    penup()
    goto(-75, -5)
    pendown()
    begin_fill()
    circle(20)
    end_fill()
    penup()
    goto(75, -10)
    pendown()
    begin_fill()
    circle(25)
    end_fill()
    penup()
    goto(75, -160)
    pendown()
    begin_fill()
    circle(25)
    end_fill()
    penup()
    goto(-75, -160)
    pendown()
    begin_fill()
    circle(25)
    end_fill()
    penup()
    #Dibuja los puntos de intereseccion
    penup()
    goto(100, 200)
    dot(10, 0, 0, 0)
    goto(120, 240)
    dot(10, 0, 0, 0)

    goto(-55, -120)
    dot(10, 0, 0, 0)
    goto(57, 1)
    dot(10, 0, 0, 0)
    goto(71, 40)
    dot(10, 0, 0, 0)
    goto(86, -110)
    dot(10, 0, 0, 0)
    goto(-50, -141)
    dot(10, 0, 0, 0)
    goto(-99, 5)
    dot(10, 0, 0, 0)
    #Escribir etiquetas de texto
    goto(-150, 200)
    write("ready", False, "center", ("arial", 10, "bold italic"))
    goto(0, 200)
    write("data in", False, "center")
    goto(150, 270)
    write("timeout", False, "center")
    goto(150, 200)
    write("sending", False, "center", ("arial", 10, "bold italic"))
    goto(0, 75)
    write("ack", False, "center")
    goto(-75, 15)
    write("qo", False, "center", ("arial", 10, "bold italic"))
    goto(75, 15)
    write("q1", False, "center", ("arial", 10, "bold italic"))
    goto(75, -135)
    write("q3", False, "center", ("arial", 10, "bold italic"))
    goto(-75, -135)
    write("q2", False, "center", ("arial", 10, "bold italic"))
    goto(0, 65)
    write("1", False, "center")
    goto(0, 5)
    write("1", False, "center")
    goto(120, -60)
    write("0", False, "center")
    goto(20, -60)
    write("0", False, "center")
    goto(-120, -60)
    write("0", False, "center")
